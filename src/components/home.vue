  methods: {
    async logout() {
      const flag = await this.$confirm('此操作将退出登录, 是否继续?', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      }).catch(() => {
        this.$message({
          type: 'info',
          message: '已取消'
        })
      })
      console.log(flag)
      if (flag == 'confirm') {
        this.$router.push('/login')
        window.sessionStorage.removeItem('token')
      }
    }
  }
}
